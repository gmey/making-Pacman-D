	public void setUp() {
		/*loading the background of the maze*/
		backGround();
		/*this how i fill the maze details*/
		for (int i = 0; i < 28; i++) {
			for (int j = 0; j < 31; j++) {
				tileRep[j][i] = new Tiles(i * 16 + 8, j * 16 + 8);
				switch (mazeMap[j][i]) {
				case 0:
					tileRep[j][i].point = true;
					break;

				case 1:
					tileRep[j][i].wall = true;
					break;

				case 6:
					tileRep[j][i].space = true;
					break;

				case 8:
					tileRep[j][i].bigPoint = true;
					break;
				}
			}
		}
		/*drawing points*/
		drawPoints();
	}
/**
 * this is for loading the image into the background.
 */
	private void backGround() {
		ImageView imgView = new ImageView(mapImage);
		Main.getRoot().getChildren().add(imgView);
	}

	public void drawPoints() {
		for (int i = 0; i < 28; i++) {
			for (int j = 0; j < 31; j++) {
				tileRep[j][i].show();
			}
		}
	}

